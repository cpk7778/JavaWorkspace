package chap_01;

public class _02_DataTypes {
    public static void main(String[] args) {
        System.out.println("Hello World!"); //문자
        System.out.println("안녕하세요");
        System.out.println(12); //숫자
        System.out.println(-34);
        System.out.println(3.14);
        System.out.println(true); //논리
        System.out.println(false); //거짓
        System.out.println(123 + 345); //연산 더하기
        System.out.println(2 * 564); //연산 곱하기
        System.out.println(123 + 345 * 2); //연산 더하기 곱하기

    }
}
